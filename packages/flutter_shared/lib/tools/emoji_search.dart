  /// 按分类获取
  static List<(String, String, String)> byCategory(String category) {
    return all.where((e) => e.$3 == category).toList();
  }

  /// 搜索
  static List<(String, String, String)> search(String query) {
    if (query.isEmpty) return all;
    final q = query.toLowerCase();
    return all.where((e) =>
      e.$2.toLowerCase().contains(q) ||
      e.$1.contains(q)
    ).toList();
  }
